/**
 * 加法
 *
 * @param x x值
 * @param y y值
 * @returns
 */
export function plus(x: number, y: number = x) {
  return x + y;
}


/**
 * 减法
 *
 * @param x x值
 * @param y y值
 * @returns
 */
export function minus(x: number, y: number = x) {
  return x - y;
}


/**
 * 乘法
 *
 * @param x x值
 * @param y y值
 * @returns
 */
export function times(x: number, y: number = x) {
  return x * y;
}


/**
 * 除法
 *
 * @param x x值
 * @param y y值
 * @returns
 */
export function divided(x: number, y: number = x) {
  return x / y;
}


/**
 * 取模
 *
 * @param x x值
 * @param y y值
 * @returns
 */
export function modulo(x: number, y: number = x) {
  return x % y;
}


/**
 * 取幂
 *
 * @param x x值
 * @param y y值
 * @returns
 */
export function power(x: number, y: number = x) {
  return x ** y;
}



